"""
tests/test_sigmund_model_hierarchy.py

Pins the contract that core/llm/llm_providers.json declares chat_sigmund as a
genuinely higher-fidelity LLM lineup than chat_assistant — not merely a
re-ordering of the same models.

Three required assertions (per spec):
  1. The #0 (preferred) model differs between chat_assistant and chat_sigmund.
  2. The #0 model in chat_sigmund is of higher fidelity than chat_assistant's #0.
  3. The two model arrays are not the same set in a different order.

Plus three defensive assertions:
  4. Both task entries actually exist with non-empty `models` arrays.
  5. The arrays go through the live LLMProviderConfig loader path (indirect
     test) and produce the same answer (so the loader's defaults can never
     silently mask a missing config).
  6. Every model named in chat_assistant or chat_sigmund is ranked in
     data/model_hierarchy.json (per spec: an unknown model aborts the test
     and demands human intervention before deployment).

Source of truth for the fidelity ladder is data/model_hierarchy.json,
generated by scripts/probe_models.py from the providers' live model-list
endpoints. The ladder is NOT embedded in this test file — that was the v2
mistake. To regenerate after a new model ships:

    ./scripts/probe_models.py --write

Schema note: llm_providers.json is shaped as
    {
      "providers": { "gemini": {...}, "claude": {...}, "openai": {...} },
      "routing":   { "chat_assistant": {...}, "chat_sigmund": {...}, ... }
    }
…NOT with task entries at the top level. Tests read from cfg["routing"][task].
"""

